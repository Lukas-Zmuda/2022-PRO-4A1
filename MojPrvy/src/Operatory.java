
public class Operatory {

	public static void main(String[] args) {
		
		
		int x = 10;
		
		x = x + 5;
		x += 5;
		
		x = +5;
		System.out.println(x);
		
		
		x -= 5;
		x *= 5;
		
		x++;
		x--;
		++x;
		
		x = 10;
		int y = ++x;
		
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		

	}

}
